function fakeImage(string $model, ?string $image = null): string
{
    $dir = storage_path("app/public/$model");

    if (!is_dir($dir)) {
        mkdir($dir, 0777, true);
    }

    if (is_null($image)) {
        $image = rand(1, 9) . '.png';
    }

    if (!file_exists("$dir/$image")) {
        File::copy(storage_path("fake/$image"), "$dir/$image");
    }

    return "$model/$image";
}

function a(string $path): string
{
    return Vite::asset('resources/' . $path);
}

function get_only_numbers($value): string
{
    return preg_replace('/[^0-9]/', '', $value);
}
